    private void jtpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtpesquisarActionPerformed
        // TODO add your handling code here:
        
        helperContas helpercontas = helperContas.getInstance();
        contaCorrente temp = null;
        int codigo = 0;
        codigo = Integer.parseInt(jTcodigo.getText());
        try{
            codigo = Integer.parseInt(jTcodigo.getText().trim());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite um código: ");
            return;
        }

        temp = helpercontas.pesquisarCorrente(codigo);
        if (temp != null) {
            JOptionPane.showMessageDialog(this, "Objeto encontrado." + temp);
            jTcodigo.setText(""+ temp.getCodigo());
            jtnome.setText("" + temp.getNome());
            jtendereco.setText("" + temp.getEndereco());
            jtcpf.setText("" + temp.getCpf());
            jtrg.setText("" + temp.getRg());
            jtrenda.setText("" + temp.getRenda());
            jtagencia.setText("" + temp.getAgencia());
        } else {
            JOptionPane.showMessageDialog(this, "Código não encontrado.");
        }

        
    }//GEN-LAST:event_jtpesquisarActionPerformed

