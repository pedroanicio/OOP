    private void btincluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btincluirActionPerformed
        contaCorrente temp = null;
        String nome = null, enderec = null;
        long cpf = 0, rg = 0;
        double renda = 0;
        int agen = 0;
        int codigo = 0;
        
        nome = jtnome.getText();
        if (nome.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Campo nome inválido.");
            return;
        }
        enderec = jtendereco.getText();
        if (enderec.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Campo endereço inválido.");
            return;
        }
        
        try{
            cpf = Integer.parseInt(jtcpf.getText().trim());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite o cpf: ");
            return;
        }
        try{
            rg = Integer.parseInt(jtrg.getText().trim());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite o RG: ");
            return;
        }
        try{
            renda = Double.parseDouble(jtrenda.getText().trim());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite a renda: ");
            return;
        }
        
        try{
            agen = Integer.parseInt(jtagencia.getText().trim());
            if(agen == helperAgencia.getInstance().pesquisar(Integer.parseInt(jtagencia.getText().trim()))){
                jtagencia.setText(""+ agen);
                codigo = helperContas.getInstance().soma();
                temp = new contaCorrente(nome, enderec, cpf, rg, renda, agen, codigo);
                helperContas.getInstance().addContaCorrente(temp);
        
                jTcodigo.setText(Integer.toString(codigo));
                JOptionPane.showMessageDialog(this, "Objeto incluído!");
           }else{
                JOptionPane.showMessageDialog(this, "Agencia inexistente.");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite uma agencia valida: ");
            return;
        }        

    }//GEN-LAST:event_btincluirActionPerformed

    private void jbexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbexcluirActionPerformed
        // TODO add your handling code here:
        int codigo;
        try{
            codigo = Integer.parseInt(jTcodigo.getText().trim());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Digite um código: ");
            return;
        }
        
        helperContas helpercorrente = helperContas.getInstance();
        int totalObjetos = 0;
        totalObjetos = helpercorrente.excluirCorrente(codigo);
        if (totalObjetos > 0) {
            JOptionPane.showMessageDialog(this, "Total de objetos excluidos: " + totalObjetos);
        } else {
            JOptionPane.showMessageDialog(this, "Código não encontrado.");
        }
        
    }//GEN-LAST:event_jbexcluirActionPerformed

