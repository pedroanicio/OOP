    private void jbexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbexcluirActionPerformed
        // TODO add your handling code here:
        
        int codigo = 0;
        try {
            codigo = Integer.parseInt(jTcodigo1.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Digite um valor para excluir.");
            return;
        }
        helperAgencia agenciaHelper = helperAgencia.getInstance();
        int totalObjetos = 0;
        totalObjetos = agenciaHelper.excluir(codigo);
        if (totalObjetos > 0) {
            JOptionPane.showMessageDialog(this, "Total de objetos excluidos: " + totalObjetos);
        } else {
            JOptionPane.showMessageDialog(this, "Código não encontrado.");
        }
    }//GEN-LAST:event_jbexcluirActionPerformed

    private void jbalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbalterarActionPerformed
        // TODO add your handling code here:
        
        int codigo = 0;
        String descricao;
        try {
            codigo = Integer.parseInt(jTcodigo1.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Digite um valor para alterar.");
            return;
        }

        descricao = jTdescricao.getText();
        if (descricao.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Campo descrição inválido.");
            return;
        }

        Agencia agencia = new Agencia(codigo, descricao);
        if (helperAgencia.getInstance().alterar(agencia) >= 0) {
            JOptionPane.showMessageDialog(this, "Objeto alterado.");
        } else {
            JOptionPane.showMessageDialog(this, "Objeto não slterado.");
        }

        
    }//GEN-LAST:event_jbalterarActionPerformed

