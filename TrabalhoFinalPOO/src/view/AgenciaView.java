    private void btincluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btincluirActionPerformed
        Agencia temp = null;
        
        int codigo = 0;
        String descricao = null;
        try {
            codigo = Integer.parseInt(jTcodigo1.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido.");
            return;
        }

        descricao = jTdescricao.getText();
        if (descricao.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Campo descrição inválido.");
            return;
        }
        temp = new Agencia(codigo, descricao);
        helperAgencia.getInstance().addAgencia(temp);
        JOptionPane.showMessageDialog(this, "Objeto incluído!");
    }//GEN-LAST:event_btincluirActionPerformed

